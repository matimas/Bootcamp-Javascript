let numOfSiblings = parseInt(prompt('How many siblings do you have?', 0));
if (numOfSiblings === 1) {
	console.log('1 Siblings!');
} else if (numOfSiblings > 1) {
	console.log('More than 1 Sibling');
} else {
	console.log('NO Siblings');
}
